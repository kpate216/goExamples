// learning values in go
package main

import "fmt"

func main() {

	// strings can be added together
	fmt.Println("krupa" + "patel")

	//integers
	fmt.Println("1+1 =", 1+1)

	//floats
	fmt.Println("7.0/3.0 =", 7.0/3.0)

	//booleans, with boolean operators
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
